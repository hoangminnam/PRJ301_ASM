package model;

/**
 *
 * @author hoang
 */
public class Term {
    private int termID;
    private String tname;

    public Term() {
    }

    public Term(int termID, String tname) {
        this.termID = termID;
        this.tname = tname;
    }

    public int getTermID() {
        return termID;
    }

    public void setTermID(int termID) {
        this.termID = termID;
    }

    public String getTname() {
        return tname;
    }

    public void setTname(String tname) {
        this.tname = tname;
    }
}
